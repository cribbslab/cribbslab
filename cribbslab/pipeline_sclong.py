@follows(mkdir("qc_nanoplot"))
@transform(align_minimap2,
           regex(r"aligned/(\S+)\.bam"),
           r"qc_nanoplot/\1/NanoStats.txt")
def qc_nanoplot(infile, outfile):
    """
    Run NanoPlot on aligned BAM files for long-read QC metrics.
    """

    outdir = os.path.dirname(outfile)

    job_threads = PARAMS.get("nanoplot_threads", 4)
    job_memory = PARAMS.get("nanoplot_memory", "8G")

    statement = """
        NanoPlot --bam %(infile)s 
        -o %(outdir)s 
        --tsv_stats 
        --plots dot 
        -t %(job_threads)s
    """

    P.run(statement)


# -----------------------------------------------
# QC: Cell barcode statistics
# -----------------------------------------------

@follows(mkdir("qc_cells"))
@merge(run_blaze, "qc_cells/barcode_summary.txt")
def summarize_barcodes(infiles, outfile):
    """
    Summarize cell barcode assignment statistics across all samples.
    """

    import pandas as pd

    dfs = []
    for infile in infiles:
        sample = os.path.basename(infile).replace("_barcode_stats.txt", "")
        df = pd.read_csv(infile, sep="\t")
        df["sample"] = sample
        dfs.append(df)

    combined = pd.concat(dfs, ignore_index=True)
    combined.to_csv(outfile, sep="\t", index=False)


# -----------------------------------------------
# MultiQC: Aggregate reports
# -----------------------------------------------

@follows(qc_nanoplot, count_genes, summarize_barcodes)
@merge([align_minimap2, "qc_nanoplot/*", "genecounts/*"],
       "multiqc/multiqc_report.html")
def multiqc(infiles, outfile):
    """
    Aggregate all QC reports using MultiQC.
    """

    outdir = os.path.dirname(outfile)

    statement = """
        export LC_ALL=en_US.UTF-8 &&
        export LANG=en_US.UTF-8 &&
        multiqc -f -n multiqc_report.html -o %(outdir)s .
    """

    P.run(statement)


